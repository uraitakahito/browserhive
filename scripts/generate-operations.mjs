/**
 * Generate operation constants from the OpenAPI spec.
 *
 * Reads `src/http/openapi.yaml`, walks each `paths.<path>.<method>`, and
 * writes `src/http/generated/operations.gen.ts` exposing:
 *   - `OPERATIONS`: a typed `as const` mapping from `operationId` to its
 *     `{ method, path }` pair.
 *   - `OperationId`: union of all operationIds defined in the spec.
 *   - `OperationMethod`: union of HTTP methods used.
 *
 * Server-side SoT for path/method: `src/http/server.ts` imports OPERATIONS
 * to register routes instead of hardcoding paths. `createCaptureHandlers`
 * uses `Record<OperationId, RouteHandlerMethod>` so renaming an operationId
 * in the YAML surfaces as a TypeScript error in handler code.
 *
 * Must run AFTER `openapi:generate` (hey-api), because `openapi-ts` cleans
 * the output directory before writing. The `openapi:prep` script orders the
 * pipeline accordingly.
 */
import SwaggerParser from "@apidevtools/swagger-parser";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));
const SOURCE = resolve(ROOT, "src/http/openapi.yaml");
const OUTPUT = resolve(ROOT, "src/http/generated/operations.gen.ts");

/** OpenAPI 3.1 fixed-field HTTP method names. */
const HTTP_METHODS = [
  "get",
  "put",
  "post",
  "delete",
  "options",
  "head",
  "patch",
  "trace",
];

const document = await SwaggerParser.parse(SOURCE);

const collected = [];
for (const [path, pathItem] of Object.entries(document.paths ?? {})) {
  if (!pathItem) continue;
  for (const method of HTTP_METHODS) {
    const operation = pathItem[method];
    if (!operation?.operationId) continue;
    collected.push({ id: operation.operationId, method, path });
  }
}

collected.sort((a, b) => a.id.localeCompare(b.id));

const seen = new Set();
for (const op of collected) {
  if (seen.has(op.id)) {
    throw new Error(`Duplicate operationId in spec: ${op.id}`);
  }
  seen.add(op.id);
}

if (collected.length === 0) {
  throw new Error(`No operations with operationId found in ${SOURCE}`);
}

const lines = [
  "// Auto-generated by scripts/generate-operations.mjs. Do not edit.",
  "// Source: src/http/openapi.yaml",
  "",
  "export const OPERATIONS = {",
  ...collected.map(
    (op) =>
      `  ${op.id}: { method: ${JSON.stringify(op.method)}, path: ${JSON.stringify(op.path)} },`,
  ),
  "} as const;",
  "",
  "export type OperationId = keyof typeof OPERATIONS;",
  'export type OperationMethod = (typeof OPERATIONS)[OperationId]["method"];',
  "",
];

mkdirSync(dirname(OUTPUT), { recursive: true });
writeFileSync(OUTPUT, lines.join("\n"));
console.log(`generated ${OUTPUT}`);
